#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xFE,0x69,0x59,0x51,0x41,0x37,0x88,0x3C,0x9C,0x00,0x7C,0x8B,
		0x15,0x8B,0xD3,0xDA,0x1E,0xBD,0xC3,0x88,0xDD,0x9F,0x9D,0x39,
		0x8C,0xFD,0xFE,0xAD,0xA1,0xF2,0x32,0x5F,0x93,0x13,0x50,0x8F,
		0x8A,0x2F,0xA0,0x01,0xD7,0x54,0x52,0x5C,0x6F,0x2C,0x76,0x35,
		0x66,0xED,0xDF,0x1B,0xF2,0xF4,0xD5,0x57,0x6E,0xE4,0x78,0xF6,
		0x76,0xA3,0xF2,0x1B,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
